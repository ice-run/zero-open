        />
      </el-form-item>
      <el-form-item label="权限编码：" prop="code">
        <el-input
          v-model="form.code"
          placeholder="请输入权限编码"
          clearable
          class="w-[180px]!"
        />
      </el-form-item>
      <el-form-item label="状态：" prop="valid">
